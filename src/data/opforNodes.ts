// Helper to group nodes for the Sidebar Palette
export function getNodeCategories() {
  const tactics: MitreTactic[] = [
    'TA0001', 'TA0002', 'TA0003', 'TA0004', 'TA0005', 
    'TA0006', 'TA0007', 'TA0008', 'TA0009', 'TA0010', 'TA0011', 'control'
  ];

  return tactics
    .map(tactic => ({
      tactic,
      nodes: opforNodeDefinitions.filter(n => n.tactic === tactic)
    }))
    .filter(cat => cat.nodes.length > 0);
}